package model;

public class Program {
    private String name;
    private int space;
    private int useTime;


    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getSpace() {
        return space;
    }
    public void setSpace(int space) {
        this.space = space;
    }
    public int getUseTime() {
        return useTime;
    }
    public void setUseTime(int useTime) {
        this.useTime = useTime;
    }
}
